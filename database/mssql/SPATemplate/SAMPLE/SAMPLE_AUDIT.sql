CREATE TABLE [SAMPLE].[SAMPLE_AUDIT]
(
  [IdAudit] BIGINT NOT NULL IDENTITY(1,1) PRIMARY KEY,
  [IdUser] BIGINT NOT NULL FOREIGN KEY REFERENCES [ACCOUNT].[USERS],
  [IdTarget] INT NOT NULL FOREIGN KEY REFERENCES [DICT].[AUDIT_TARGETS],
  [IdAction] INT NOT NULL FOREIGN KEY REFERENCES [DICT].[AUDIT_ACTIONS],
  [Stamp] DATETIME NOT NULL,
  [TargetId] BIGINT FOREIGN KEY REFERENCES [SAMPLE].[SAMPLE]([IdSample]),
  [TargetName] VARCHAR(255),
  [Message] VARCHAR(255)
)

CREATE TABLE [SAMPLE].[SAMPLE]
(
  [IdSample] BIGINT NOT NULL IDENTITY(1,1) PRIMARY KEY,
  [IdDistrict] INT NOT NULL FOREIGN KEY REFERENCES [DICT].[DISTRICTS],
  [IdType] INT NOT NULL FOREIGN KEY REFERENCES [DICT].[SAMPLE_TYPES],
  [IdDict] INT NOT NULL FOREIGN KEY REFERENCES [DICT].[SAMPLE_DICTS],
  [Text] VARCHAR(120),
  [Number] BIGINT,
  [Date] DATE,
  [Timestamp] DATETIME,
  [Sum] DECIMAL(10,2)
)
